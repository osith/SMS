    private void txt_account_checkKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_account_checkKeyTyped
        String input = txt_account_check.getText();
        String sql = "SELECT * from teacher_personal_details WHERE teacher_fullname LIKE ?";
       
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, "%"+input+"%");
            rs = pst.executeQuery();
            if(rs != null){
            
                table_accounts.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (SQLException ex) {
            Logger.getLogger(adminUserAccounts.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_txt_account_checkKeyTyped

